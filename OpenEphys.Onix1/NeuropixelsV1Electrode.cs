        public NeuropixelsV1Bank Bank { get; private set; }

        /// <summary>
        /// Gets the column index of this electrode.
        /// </summary>
        public int ColumnIndex { get; private set; }

        /// <summary>
        /// Initializes a new instance of <see cref="NeuropixelsV1Electrode"/> using the given index.
        /// </summary>
        /// <param name="index">Integer defining the zero-indexed position of the electrode in the probe.</param>
        /// <param name="probeType">The <see cref="NeuropixelsV1ProbeType"/> of the current probe.</param>
        public NeuropixelsV1Electrode(int index, NeuropixelsV1ProbeType probeType)
        {
            Index = index;
            Shank = 0;
            IntraShankElectrodeIndex = index;
            Bank = (NeuropixelsV1Bank)(index / NeuropixelsV1.ChannelCount);
            ColumnIndex = GetColumnIndex(index, probeType);
            Channel = GetChannelNumber(index, probeType);
            var position = NeuropixelsV1eProbeGroup.DefaultContactPosition(index, probeType);
            Position = new PointF(position[0], position[1]);
        }

        /// <summary>
        /// Static method returning the channel number of a given electrode.
        /// </summary>
        /// <param name="electrodeIndex">Integer defining the index of the electrode in the probe.</param>
        /// <param name="probeType">The <see cref="NeuropixelsV1ProbeType"/> of the current probe.</param>
        /// <returns>An integer between 0 and 383 defining the channel number.</returns>
        public static int GetChannelNumber(int electrodeIndex, NeuropixelsV1ProbeType probeType)
        {
            return probeType switch
            {
                NeuropixelsV1ProbeType.NP1 => electrodeIndex % NeuropixelsV1.ChannelCount,
                NeuropixelsV1ProbeType.UHD => channelOrder[electrodeIndex],
                _ => throw new InvalidEnumArgumentException(nameof(NeuropixelsV1ProbeType))
            };
        }

        internal static int GetColumnIndex(int electrodeIndex, NeuropixelsV1ProbeType probeType)
        {
            var numColumns = NeuropixelsV1eProbeGroup.GetNumberOfColumns(probeType);

            if (probeType == NeuropixelsV1ProbeType.NP1)
            {
                return electrodeIndex % numColumns;
            }
            else if (probeType == NeuropixelsV1ProbeType.UHD)
            {
                int[] columnOrder = { 0, 7, 1, 6, 2, 5, 3, 4 };

                return columnOrder[electrodeIndex % numColumns];
            }
            else
                throw new InvalidEnumArgumentException(nameof(NeuropixelsV1ProbeType));
        }

